export function translateModel(model, lang) {
  if (!model || lang === "es") return model;
  const tr = MODEL_I18N[model.slug] && MODEL_I18N[model.slug][lang];
  return tr ? { ...model, ...tr } : model;
}

export function getLang() {
  try { return localStorage.getItem("papertok_lang") === "en" ? "en" : "es"; } catch (e) { return "es"; }
}

export function setLang(lang) {
  try { localStorage.setItem("papertok_lang", lang); } catch (e) {}
}
